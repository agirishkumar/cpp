#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    double x=4, y=12.25, z=0.0121;
    cout <<"\n Number \t Square Root"<<endl;
    cout <<"\n " << x << " \t   "<< sqrt(x)
         <<"\n " << y << " \t   "<< sqrt(y)
         <<"\n " << z << " \t   "<< sqrt(z)<<endl;

    return 0;
}